create table if not exists resume(
  resume_id varchar(10) primary key,
  name varchar(10),
  age varchar(2),
  sex varchar(1),
  domain varchar(20),
  preferences varchar(30),
  experience varchar(30)
)

create table if not exists candidate(
  candidate_id varchar(10) primary key,
  username varchar(40),
  password varchar(15),
  resume_id varchar(10) ,
  comp_id varchar(10),
  foreign key(resume_id) references resume(resume_id) on delete cascade,
  foreign key(resume_id) references resume(resume_id) on update cascade
)

create table if not exists resume_education(
  resume_id varchar(10),
  education varchar(20),
  foreign key(resume_id) references resume(resume_id) on delete cascade,
  foreign key(resume_id) references resume(resume_id) on update cascade
)

create table if not exists country(
  country_id varchar(10) primary key,
  name varchar(15),
  permit_req varchar(20)
)

create table if not exists company(
  company_id varchar(10) primary key,
  name varchar(15),
  domain varchar(20),
  turnover varchar(20),
  no_of_employees varchar(20),
  country_id varchar(10) ,
  foreign key(country_id) references country(country_id) on delete cascade,
  foreign key(country_id) references country(country_id) on update cascade
)

create table if not exists job_description(
  job_id varchar(10) primary key,
  domain varchar(20),
  post varchar(15),
  salary varchar(15),
  working_hours varchar(5),
  experience varchar(5),
  education varchar(20),
  comp_id varchar(10) ,
  foreign key(comp_id) references company(company_id)
)

create table if not exists interviewed_by(
  cand_id varchar(10) ,
  jo_id varchar(10),
  int_date date,
  int_time time,
  status varchar(10),
  foreign key(cand_id) references candidate(candidate_id),
  foreign key(jo_id) references job_description(job_id)
)

create table if not exists applied_for(
  cand_id varchar(10) ,
  job_id varchar(10) ,
  date_applied date,
  foreign key(cand_id) references candidate(candidate_id),
  foreign key(job_id) references job_description(job_id)
)

create table if not exists has_permit_for(
  candidate_id varchar(10) ,
  country_id varchar(10),
  duration varchar(10),
  foreign key(candidate_id) references candidate(candidate_id),
  foreign key(country_id) references country(country_id)
)
